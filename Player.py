class Player:

	def __init__(self, score):
		this.score = score

	def __eq__(self, other):
		"""
		__eq__(self, other) returns true if the scores of
		the two given players are equal, false otherwise.
		"""
		return self.score == other.score

	def score():
		return this.score

	

